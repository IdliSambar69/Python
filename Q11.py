set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

print("Set 1:", set1)
print("Set 2:", set2)
print("Common:", set1 & set2)
print("Only in Set 1:", set1 - set2)
print("Combined:", set1 | set2)
print("Either but not both:", set1 ^ set2)
print("Length of Set 1:", len(set1))
print("Length of Set 2:", len(set2))
