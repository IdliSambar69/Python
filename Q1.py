a = 10 
b = 4 

# Bitwise Operators
print("Bitwise AND (a & b):", a & b)
print("Bitwise OR (a | b):", a | b)
print("Bitwise XOR (a ^ b):", a ^ b)
print("Bitwise NOT (~a):", ~a)
print("Left Shift (a << 1):", a << 1)
print("Right Shift (b >> 1):", b >> 1)

# Logical Operators
x = True
y = False

print("Logical AND (x and y):", x and y)
print("Logical OR (x or y):", x or y)
print("Logical NOT (not x):", not x)


